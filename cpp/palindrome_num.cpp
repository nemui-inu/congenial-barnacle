#include <iostream>

using namespace std;

int reverse_int(int x){
  long int reverse = 0;
  while(0 < x){
    int r = x % 10;
    reverse = reverse * 10 + r;
    x /= 10;
  }
  return reverse;
}

class Solution {
public:
    bool isPalindrome(int x) {
      int rev = reverse_int(x);
      return (rev == x) ? true : false; 
    }
};

int main(){
  int n = 0, r = 0;
  
  cout << "Input: ";
  cin >> n;
  
  Solution input;

  (input.isPalindrome(n)) ? cout << "Input " << n << " is Palindrome." << endl :\
  cout << "Input " << n << " is not Palindrome." << endl;
  
  
  return 0;
}

