#include <stdio.h>
#include <stdlib.h>

int indices[2] = {0, 0}, *p = indices;

int *solution(int arr[], int target, size_t size){
  for(int i = 0; i < size; i++){
    for(int j = 0; j < size; j++){
      int sum = arr[i] + arr[j];
      if(sum == target){
        indices[0] = i;
        indices[1] = j;
      }
    }
  }

  for(int i = 0; i < size; i++){
    printf("arr[%d]: %d\n", i, arr[i]);
  }

  printf("reads size: %d\n", size);
  printf("function returns: { %d, %d }\n", indices[0], indices[1]);
  return p;
}

int main(){
  size_t size = 0;
  printf("Enter array size: ");
  scanf("%d", &size);

  int my_arr[12], *p = my_arr;
  if(size < 12 && 0 < size){
    for(int i = 0; i < size; i++){
      printf("Enter array values: ");
      scanf("%d", &my_arr[i]);
    }
  }
  else{
      printf("Error: Invalid size. Input should be lower than 12");
  }

  int target;
  printf("Enter TARGET value: ");
  scanf("%d", &target);

  int r_index[2], *r;
  r = solution(my_arr, target, size);
  printf("Indices that sum up to target: ");
  for(int i = 0; i < 2; i++){
    printf("%d ", *(r + i));
  }

  return 0;
}

