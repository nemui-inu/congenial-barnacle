#include <stdio.h>

int romanToInt(char * s){
    int i = 0;
    int val = 0;

    while(s[i] != '\0'){
      switch(s[i]){
        case 'I': 
          switch(s[i+1]){
            case 'V':
              val += 4;
              i++;
              break;
            case 'X':
              val += 9;
              i++;
              break;
            default:
              val += 1;
          }
          break;
        case 'V': 
          val += 5;
          break;
        case 'X': 
          switch(s[i+1]){
            case 'L':
              val += 40;
              i++;
              break;
            case 'C':
              val += 90;
              i++;
              break;
            default:
              val += 10;
          }
          break;
        case 'L': 
          val += 50;
          break;
        case 'C': 
          switch(s[i+1]){
            case 'D':
              val += 400;
              i++;
              break;
            case 'M':
              val += 900;
              i++;
              break;
            default:
              val += 100;
          }
          break;
        case 'D': 
          val += 500;
          break;
        case 'M':
          val += 1000;
          break;
        default:
          printf("Invalid.\n");
          return -1;
      }
      i++;
    }
    return val;
}

int main(){
  char s[32], *ps = s;

  printf("Enter Roman Numeral: ");
  //fgets(s, 32, stdin);
  scanf("%s", &s);
  
  int intval = romanToInt(ps);
  printf("%d\n", intval);

  return 0;
}

